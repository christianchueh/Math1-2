# Tab 2：多項式加減與去括號變號
# ==========================================
with tab2:
    st.subheader("🗂️ 同類項加減：分類打包的藝術")
    st.markdown(r"""
    多項式的加減法其實是一場**物流分類遊戲**。
    * **核心心法：** 只有「英文字母相同」且「次方相同」的項，才可以把前面的係數相加減。
    * 🎒 想像你的 RPG 背包：$x^2$ 叫「鑽石大箱」，$x$ 叫「金幣小盒」，常數項叫「生命藥水」。不同種類的箱子在加減時絕對不能混在一塊算！
    """)

    st.error("### ☠️ 會考粗心大魔王：去括號變號陷阱")
    st.markdown(r"""
    遇到多項式相減時，只要括號前面是**「減號」**，去括號的時候，括號裡面的**每一項都要變號**！
    $$-(3x^2 - 5x + 7) = -3x^2 + 5x - 7$$
    許多同學只變了第一項，後面就忘記變號（負負得正），這在非選擇題裡會直接被扣乾淨！
    """)

    st.markdown("---")
    st.subheader("🧠 經典代表題型精講")

    st.markdown("""
    #### 📝 【代表題 2：乘法公式與減法變號混合挑戰】(講義第116頁核心題)
    計算 $(2x - 3)^2 - (3x + 4)(3x - 4)$ 的結果。

    **🤔 引導思考：**
    這題是一道完美的複合防禦題，我們分三個步驟穩紮穩打拆解：

    * **第一步：展開前半段（套用差的平方公式）**
      $$(2x - 3)^2 = (2x)^2 - 2(2x)(3) + 3^2 = 4x^2 - 12x + 9$$

    * **第二步：展開後半段（套用平方差公式）**
      $$(3x + 4)(3x - 4) = (3x)^2 - 4^2 = 9x^2 - 16$$

    * **第三步：兩式相減（注意去括號與變號的魔鬼細節！）**
      我們把兩段結果用括號保護起來相減：
      $$原式 = (4x^2 - 12x + 9) - (9x^2 - 16)$$
      小心把中間的減號分配進去：
      $$\Rightarrow 4x^2 - 12x + 9 - 9x^2 \\mathbf{+ 16}$$
      *(注意看！$-(-16)$ 變成了 $+16$，這就是魔鬼細節！)*

      最後，把同類項（鑽石跟鑽石、常數跟常數）打包在一起：
      $$\Rightarrow (4 - 9)x^2 - 12x + (9 + 16)$$
      $$\Rightarrow \\mathbf{-5x^2 - 12x + 25}$$
    """)

    st.markdown("---")
    st.markdown("""
    #### 📝 【代表題 3：多項式逆向還原題】
    若兩個多項式相加的結果為 $5x^2 + x - 4$，已知其中一個多項式為 $2x^2 - x + 1$，求另一個多項式。

    **🤔 引導思考：**
    這跟小學數學一樣：$$\\text{甲} + \\text{乙} = \\text{總和} \\Rightarrow \\text{乙} = \\text{總和} - \\text{甲}$$
    我們直接列式，一樣要用括號把多項式保護好，準備應對減法變號：

    $$\\text{另一個多項式} = (5x^2 + x - 4) - (2x^2 - x + 1)$$
    $$\Rightarrow 5x^2 + x - 4 - 2x^2 + x - 1$$
    $$\Rightarrow (5 - 2)x^2 + (1 + 1)x + (-4 - 1)$$
    $$\Rightarrow \\mathbf{3x^2 + 2x - 5}$$
    """)

