with tab1:
    st.subheader("💡 觀念大澄清：到底什麼是「多項式」？")
    st.markdown(r"""
    多項式（Polynomial）在數學上的嚴謹定義，是由數字與文字符號（未知數 $x$）透過**有限次的加法、減法與乘法**所連接而成的算式。

    * 📢 **老師上課敲黑板：** 未知數 $x$ 只能乖乖待在分子、或是跟別人做乘法。如果它亂跑，就直接出局！
    """)

    st.markdown("### 👿 絕不可觸犯的【三大禁忌場景】")

    # 採用標準、乾淨的 st.warning，並加入嚴謹且學生能懂的解釋
    c1, c2, c3 = st.columns(3)
    with c1:
        st.warning("❌ 禁忌 1：x 住在分母（分式）")
        st.markdown(r"**例如：** $\frac{5}{x} + 2$")
        st.caption("💡 原因：多項式只能有加、減、乘運算。當 $x$ 跑到分母時，本質上是在進行「除以 $x$」，違反了基本定義！")
    with c2:
        st.warning("❌ 禁忌 2：x 被關在根號內（根式）")
        st.markdown(r"**例如：** $\sqrt{5x-3}$")
        st.caption("💡 原因：根號內部的算式不能算出負數（國中不討論複數），這會嚴重限制未知數的自由生存，所以不屬於多項式。")
    with c3:
        st.warning("❌ 禁忌 3：x 被鎖在絕對值內")
        st.markdown(r"**例如：** $|2x + 1|$")
        st.caption(
            "💡 原因：絕對值會強行把負數扭轉成正數，破壞了原本多項式代數運算（指數、係數）的純粹規律，直接踢出多項式家族。")

    st.markdown("---")
    st.subheader("🎨 多項式的身份代號：次數、項數與排序")
    st.markdown(r"""
    一個多項式是由許多「單項」拼湊起來的。我們看它的最高次方是幾次，它就是「幾次多項式」。

    * **升冪排列**：次方由**低到高**排（像上樓梯）。
    * **降冪排列**：次方由**高到低**排（像下樓梯，最常見，一般寫直式除法前必須先降冪）。
    * ⚠️ **魔鬼細節：** 每一項前面的**正負號是跟著後面的數字和文字一起跑的**！千萬別把正負號拆散了。
    """)

    st.markdown("""
    ### 🧠 經典代表題型精講

    #### 📝 【代表題 1：未知數消失魔術（一次多項式變形）】(講義第120頁經典題)
    若常數 $a, b$ 使得算式 $(a-3)x^3 + (b+2)x^2 + 5x - 1$ 為一個 $x$ 的**一次多項式**，求 $a+b$ 的值。

    **🤔 引導思考：**
    1. **看清宣稱：** 題目說這個算式本質上是個「一次多項式」。
    2. **強制消去：** 既然是一次，代表那些比一次方還要高的項（也就是三次項 $x^3$、二次項 $x^2$）通通都是假象！我們必須強制讓它們前方的係數**歸零**，它們才會徹底消失。
    3. **解鎖未知數：** * 要讓三次項消失 $\Rightarrow a - 3 = 0 \Rightarrow \\mathbf{a = 3}$
       * 要讓二次項消失 $\Rightarrow b + 2 = 0 \Rightarrow \\mathbf{b = -2}$
    4. **最終計算：**
       此時算式只剩下 $5x - 1$，確實符合一次多項式。
       所求 $$a + b = 3 + (-2) = \\mathbf{1}$$
    """)

